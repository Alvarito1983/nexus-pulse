let currentLang = localStorage.getItem('pulse-lang') || 'en';
const listeners = new Set();

export function t(key) {
  return translations[currentLang]?.[key] || translations.en[key] || key;
}
export function getLang() { return currentLang; }
export function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('pulse-lang', lang);
  listeners.forEach(fn => fn(lang));
}
export function useLang() {
  const [lang, setLangState] = useState(currentLang);
  useEffect(() => {
    const h = l => setLangState(l);
    listeners.add(h);
    return () => listeners.delete(h);
  }, []);
  return lang;
}
export const LangSelector = ({ style = {} }) => {
  const lang = useLang();
  const btn = (l) => ({
    padding: '4px 8px',
    background: lang === l ? 'var(--bg-overlay)' : 'transparent',
    border: 'none',
    borderRadius: 'var(--radius-sm)',
    cursor: 'pointer',
    fontSize: 'var(--text-xs)',
    fontWeight: lang === l ? 'var(--weight-medium)' : 'var(--weight-normal)',
    color: lang === l ? 'var(--text-primary)' : 'var(--text-muted)',
    fontFamily: 'var(--font-sans)',
    transition: 'all var(--transition-fast)',
  });
  return (
    <div style={{
      display: 'flex', gap: 2,
      background: 'var(--bg-elevated)',
      border: '1px solid var(--border-subtle)',
      borderRadius: 'var(--radius-md)',
      padding: 2,
      ...style,
    }}>
      <button onClick={() => setLang('en')} style={btn('en')}>🇬🇧 EN</button>
      <button onClick={() => setLang('es')} style={btn('es')}>🇪🇸 ES</button>
    </div>
  );
};
export default translations;
